# Advanced Numbers

# Hexadecimal
# Convert numbers into hexadecimal
print(hex(12))
print(hex(512))

# Binary
# Will return binary representation
print(bin(128))
print(bin(512))

# Power
print(pow(2, 4))
# 3 args, the 3rd argument x to the power of y mod z
# (x**y) % z
print(pow(2, 4, 3))

# Absolute Value
# returns absolute value
print(abs(-3))
print(abs(2))

# Round
# Will round to the default position in decimal points
# default is 0, always returns floating point
print(round(3.1))
# Can specify how many decimal places
print(round(3.141592, 2))
